    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->validate([
            'name' => 'required|string|max:25|unique:genders,name'
        ]);
        
        $gender_slug = $data['name'];

        $gender = new Gender();
        $gender->name = $data['name'];
        $gender->slug = $gender_slug;
        $gender->save();

        return redirect()->route('genders.index')->with('info', 'Género guardado exitosamente');
        
    }

